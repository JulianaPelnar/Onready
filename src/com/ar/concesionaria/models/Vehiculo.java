package com.ar.concesionaria.models;

public class Vehiculo {

	protected double precio;
	protected String marca;
	protected String modelo;
	
	public double getPrecio() {
		return this.precio;
	}
	
	public String getMarca() {
		return this.marca;
	}
	
	public String getModelo() {
		return this.modelo;
	}
	
}
